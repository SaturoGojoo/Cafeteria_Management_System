public class largestOfTwo {
    public static void main(String args[]){
        int a=10,b=20;
        if(a>b){
            System.out.println(a+" is largest!");
        }
        else if(b>a){
            System.out.println(b+" is largest!");
        }
        else{
            System.out.println(a+" and "+b+" are equal numbers!");
        }
    }
}